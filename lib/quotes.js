/**
 * Get the daily motivational quote
 * Uses date as seed for consistency throughout the day
 */
export function getDailyQuote() {
  const today = new Date();
  const dateString = `${today.getFullYear()}-${today.getMonth()}-${today.getDate()}`;
  
  // Create a simple hash from the date string
  let hash = 0;
  for (let i = 0; i < dateString.length; i++) {
    const char = dateString.charCodeAt(i);
    hash = ((hash << 5) - hash) + char;
    hash = hash & hash; // Convert to 32bit integer
  }
  
  // Combine all quotes for variety
  const allQuotes = [
    ...morningQuotes,
    ...afternoonQuotes,
    ...eveningQuotes,
    ...habitQuotes,
    ...flowQuotes,
  ];
  
  // Use absolute value of hash to get consistent quote for the day
  const index = Math.abs(hash) % allQuotes.length;
  return allQuotes[index];
}

