/**
 * Get a quote based on time of day
 */
export function getTimeBasedQuote() {
  const hour = new Date().getHours();
  let quotes;
  
  if (hour >= 5 && hour < 12) {
    quotes = morningQuotes;
  } else if (hour >= 12 && hour < 18) {
    quotes = afternoonQuotes;
  } else {
    quotes = eveningQuotes;
  }
  
  return quotes[Math.floor(Math.random() * quotes.length)];
}

/**
 * Get a quote based on current day of week
 */
export function getDayQuote() {
  const day = new Date().getDay();
  const quotes = dayQuotes[day];
  return {
    text: quotes[Math.floor(Math.random() * quotes.length)],
    author: "Neo Routine",
  };
}

/**
 * Get a quote for the current streak status
 */
export function getStreakQuote(streak) {
  let category;
  if (streak === 0) {
    category = 'atRisk';
  } else if (streak < 7) {
    category = 'building';
  } else {
    category = 'strong';
  }
  
  const quotes = streakQuotes[category];
  return {
    text: quotes[Math.floor(Math.random() * quotes.length)],
    author: "Neo Routine",
    category,
  };
}

